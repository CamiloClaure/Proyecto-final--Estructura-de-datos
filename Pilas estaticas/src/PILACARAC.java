    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        if (!text.getText().equals("")) {
            
                String dato;
                
                    dato = text.getText();
                    for (int a = 0; a < dato.length();) {
                        Character c;
                        c = new Character(dato.charAt(a++));
                        ww.empujar(c);

                    }

                    palindrome = true;
                    for (int j = 0; palindrome && !ww.vacia();) {
                        Character c;
                        c = (Character) ww.quitar();
                        palindrome = dato.charAt(j++) == c.charValue();
                    }
                    ww.limpiar();
                    if (palindrome) 
                        JOptionPane.showMessageDialog(null, "La palabra " + dato + "es un palindromo \n");

                     else 
                        JOptionPane.showMessageDialog(null, "La palabra  es " + palindrome
                                + " no es un palindromo \n ");
                    
                }
          
        
        
        
        else {
            JOptionPane.showMessageDialog(null, "Ingrese valores");
                }
    }//GEN-LAST:event_jButton1ActionPerformed

