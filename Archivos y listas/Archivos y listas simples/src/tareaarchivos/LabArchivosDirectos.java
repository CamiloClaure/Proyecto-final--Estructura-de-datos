    public void llenarTabla() throws FileNotFoundException, IOException {
        try {
            FileReader leer = new FileReader(arc);
            BufferedReader bufalmac = new BufferedReader(leer);
            String linea;
            DefaultTableModel modelo_tabla = new DefaultTableModel();
            modelo_tabla.addColumn("Nombre");
            modelo_tabla.addColumn("Codigo");
            modelo_tabla.addColumn("Semestre");
            modelo_tabla.addColumn("Carrera");

            while ((linea = bufalmac.readLine()) != null) {
                if (linea.contains("|*")) {
                } else {
                    StringTokenizer dato = new StringTokenizer(linea, "|");
                    Vector x = new Vector();
                    while (dato.hasMoreElements()) {
                        x.addElement(dato.nextToken());
                    }

                    modelo_tabla.addRow(x);

                }

                jTable1.setModel(modelo_tabla);
            }
            leer.close();
        } catch (Exception e) {
            System.out.println(e);
        }
    }
