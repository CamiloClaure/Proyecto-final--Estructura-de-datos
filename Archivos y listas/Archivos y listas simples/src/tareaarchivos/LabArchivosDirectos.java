    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            if (arc == null) {
                switch (JOptionPane.showConfirmDialog(null, "Desea crear un archivo nuevo?")) {
                    case JOptionPane.OK_OPTION:
                        if (choose.showSaveDialog(null) == JFileChooser.APPROVE_OPTION) {

                        arc = choose.getSelectedFile();
                        arch.setTexto(arc);
                    }
                        break;
                    case JOptionPane.CLOSED_OPTION:
                        JOptionPane.showMessageDialog(null, "No hay un archivo seleccionado");
                        choose.showDialog(null, "Abrir");
                        arc = choose.getSelectedFile();
                        JOptionPane.showMessageDialog(null, "Por favor vuelva a insertar el dato");
                        break;

                }

            } else {
                try {

                    FileReader leer = null;
                    String[] datos = new String[5];
                    datos[0] = txtNombre.getText();
                    datos[1] = txtCodigo.getText();
                    datos[2] = (String) txtSemestre.getSelectedItem();
                    datos[3] = (String) txtCarrera.getSelectedItem();
                    datos[4] = "+";
                    if (rdPos.isSelected() && txtPos.getText() != " ") {

                        arch.insertarTexto(datos, arc, Integer.parseInt(txtPos.getText()));

                    } else {
                        arch.insertarTexto(datos, arc);
                    }
                } catch (Exception e) {
                    // System.out.println("Error aqui!!!");
                } finally {
                    llenarTabla();
                }

            }

        } catch (IOException ex) {
            Logger.getLogger(LabArchivosDirectos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

